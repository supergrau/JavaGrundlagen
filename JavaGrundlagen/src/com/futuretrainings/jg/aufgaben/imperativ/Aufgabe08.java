package com.futuretrainings.jg.aufgaben.imperativ;

public class Aufgabe08 {

	public static void main(String[] args) {
        int i, j;

        for (i = 10; i >= 0; i--) {
        	for (j = i; j >= 0; j--) System.out.print(j + " ");

            System.out.println();
        }

        System.out.println();

	}

}
